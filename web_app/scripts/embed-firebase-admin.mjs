/**
 * Cloud Run에는 .env.local이 없어 process.env.FIREBASE_*가 비어 Admin SDK가 실패한다.
 * next build 시점에 .env에서 읽은 값을 이 파일로 떼어 넣으면 번들에 상수로 박혀 배포 후에도 동작한다.
 * (npm run build 전에 자동 실행 — package.json prebuild)
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import dotenv from 'dotenv';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');

dotenv.config({ path: path.join(root, '.env.production.local') });
dotenv.config({ path: path.join(root, '.env.local') });
dotenv.config({ path: path.join(root, '.env.production') });
dotenv.config({ path: path.join(root, '.env') });

const json = process.env.FIREBASE_SERVICE_ACCOUNT_JSON;
const email = process.env.FIREBASE_CLIENT_EMAIL;
const rawKey = process.env.FIREBASE_PRIVATE_KEY;
const privateKey =
    typeof rawKey === 'string' ? rawKey.replace(/\\n/g, '\n').trim() : '';

const outPath = path.join(root, 'src/lib/firebaseAdminEmbedded.generated.js');

let content = `// AUTO-GENERATED by scripts/embed-firebase-admin.mjs (prebuild / predev)\n`;
content += `export const __FIREBASE_ADMIN_EMBEDDED_JSON = ${json?.trim() ? JSON.stringify(json) : 'null'};\n`;
content += `export const __FIREBASE_ADMIN_EMBEDDED_CLIENT_EMAIL = ${email?.trim() ? JSON.stringify(email.trim()) : 'null'};\n`;
content += `export const __FIREBASE_ADMIN_EMBEDDED_PRIVATE_KEY = ${privateKey.includes('BEGIN PRIVATE KEY') ? JSON.stringify(privateKey) : 'null'};\n`;

fs.writeFileSync(outPath, content, 'utf8');
console.log('[embed-firebase-admin] wrote', outPath, {
    hasJson: !!(json && json.trim()),
    hasEmailKey: !!(email && privateKey.includes('BEGIN PRIVATE KEY')),
});
